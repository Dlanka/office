                                    <h3 class="post-title" itemprop="name">IASL recognizes Top Achievers  of "Life Insurance Awareness Month" Campaign</h3>
                                    <h5>04 December 2015</h5>
                                    <div class="event-org">
                                        <!--<h4>Awareness Campaign</h4>-->
                                    </div> 
                                    <p>In celebration and commemoration of the success of the 'Life Insurance Awareness Month' campaign that was held during the month of September, the Insurance Association of Sri Lanka (IASL) hosted the Life Insurance Industry Awards Ceremony on the 4th of December 2015, at Kingsbury Hotel, Colombo for the first time </p>
                                    <p>The principle objective of the event was to recognise and reward the top sales producers and sales officers of the Life Insurance Industry, especially those who contributed to the success of the 'Life Insurance Awareness Month' campaign.  100 of the top sales officers were presented with special awards during the ceremony. Thanks to the tremendous success of the campaign the industry achieved a 30% growth in new policies in the month of  September 2015. </p>
                                    <p>The Managing Director and Vice President of LIMRA/LOMA, Mr. Ian Watts presided over the ceremony as Chief Guest while Mrs. Indrani Sugathadasa was the Guest of Honor. Other principle attendees included Mrs. Damayanthi Fernando the Director General of IBSL. The foundation of the campaign and the awards ceremony is the bond between all those in the insurance industry who worked tirelessly to ensure the development and enhancement of life insurance in Sri Lanka. This point was emphasized by Mr. Dirk Pereira, who congratulated the IASL on its "ability to bring everyone together" in his welcome speech whilst also thanking everyone for their contributions in order to ensure the success of the campaign. </p>
                                    <p>Chief Guest, Mr. Watts stated that LIMRA and LOMA were proud to add in their contributions to what he termed "an outstanding achievement". On the importance of life insurance, Mr. Watts had this to say, "life insurance is the foundation of society. Those who've had tragedy should be protected. The insurance industry exists to rebuild and protect". He also went on to graciously acknowledge the contributions of those who've worked hard in the insurance industry.</p>
                                    <p>Mr. Samitha Hemachandra the Chairman of the Marketing and Sales Forum went on to list the number of targets achieved thanks to the 'Life Insurance Awareness Month' campaign. He also thanked a number of those present for their contribution in making the campaign a success and thus enhancing the growth of the life insurance industry in Sri Lanka.  Chairperson of IBSL, Mrs. Indrani Sugathadasa expressed her satisfaction regarding the success of the programme and what had been achieved. </p>


