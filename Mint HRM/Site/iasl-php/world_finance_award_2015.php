                                    <p>World Finance, the respected UK-based magazine, has reaffirmed Ceylinco Life as the ‘Best Life Insurance Company in Sri Lanka’ for a second consecutive year, following an in-depth assessment of key performance indicators.</p>
                                    <p>The prestigious accolade is based on scores accorded to the company on multiple parameters pertaining to long term insurance. These include underwriting process/process efficiency; policy maintenance – the process of reviewing clients’ policies, appropriateness of coverage and cost per policy; exposure to risk; customer retention rate; time taken to settle claims; new customer acquisition rate and financial stability – premium income, life fund and market share.</p>
                                    <p>“Eleven consecutive years of market leadership in Sri Lanka is by itself an emphatic statement of our status as the country’s best life insurer,” Ceylinco Life Managing Director/CEO Mr R. Renganathan said. “However, an independent assessment of key performance indicators by a panel of expert international analysts is always helpful in benchmarking the company.”</p>
                                    <p>The World Finance judging panel boasts over 230 years of financial and business journalism, supported by a research team that works round the clock to ensure its award winners are the most deserving in their sector.</p>
                                    <p>For the year ending 31st December 2014, Ceylinco Life reported total income of Rs 18.54 billion with premium income of Rs 12 billion and investment income of Rs 6.83 billion. The value of the company’s investment portfolio rose to Rs 57.2 billion, assets to Rs 71.07 billion and its Life Fund to Rs 60.02 billion. </p>
                                    <p>Begun in 2007, World Finance magazine’s award programmes are tailored to provide a comprehensive analysis of the very best in each market. The judging panel is required to avoid bias towards criteria such as depth of practice and size of company, in order to get an insight into different geographies and niche areas.</p>
                                   
                                    

