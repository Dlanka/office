                                <p>In today's rapidly changing environment Life Insurance is becoming increasingly important according to Ms. Damayanthi Fernando, Director General of the Insurance Board of Sri Lanka (IBSL). The fast changing family norms and cultural values and the increasing aging population makes investing in retirement and protection based  life insurance plans all the more critical, she stressed.</p>

                                <p itemprop="description">Noting that compared with other countries, insurance penetration is low in Sri Lanka, the IBSL also sees the need for insurance companies to develop innovative insurance solutions that meet diverse needs of people whilst improving public confidence regarding the sector.</p>
                                <p itemprop="description">"As we all know, life is uncertain and in a case of sudden death of a person, his or her dependents will benefit out of a life insurance cover that has been purchased," she points out. </p>
                                <p itemprop="description">Explaining that the benefits can derive in many ways, Ms Fernando says that an insurance policy can secure the personâ€™s loved ones financially to live without depending on others. It has the ability to continue with children's education without interruption or the ability to pay any debts of the family. "Therefore, investing in life insurance gives you and your loved ones a way to live without being a burden to the family members or the State" she said.</p>
                                <p itemprop="description">September has been designated as the 'Insurance Awareness Month' by the Insurance Association of Sri Lanka.  In addition to creating awareness and promotion of life insurance through media campaigns, conducting seminars, workshops, competitions among school children and university students will have a positive impact.</p>
                                <h4>IBSL Director General on the value of insurance in an interview:</h4>
                                <div class="col-lg-11 ">
                                    <p itemprop="description"><b>Q: How is the response for insurance in other countries?</b></p>
                                    <p itemprop="description">A: Total life insurance premiums in advanced markets have grown by 3.8% in 2014, outpacing Gross Domestic Product (GDP) growth and increasing insurance penetration. In 20 of the 29 markets for which data is available, life insurance premiums growth has outpaced economic growth. In 2014, the average per capita spend on insurance (insurance density) in advanced markets was USD 3666, up around 2.5% from the previous year. Per capita spending on life insurance has risen to USD 2090.</p>

                                    <p itemprop="description"><b>Q: How does Sri Lanka compare with regard to penetration?</b></p>
                                    <p itemprop="description">A: Insurance penetration is measured as the ratio of total insurance premium in a particular year to the GDP. In 2014, the penetration level was at 1.02% and indicated a decline compared to 1.10% recorded in 2013. Penetration level of the long term insurance business, which includes life insurance was 0.46% in 2014 recording a decline from year 2013 being 0.48%.   Therefore, there is a need to develop insurance products that meet diverse needs of people whilst improving public confidence regarding the sector. Awareness and appreciation on the concept of insurance among the public is another important factor to be considered to develop the sector.</p>
                                    <p itemprop="description">Insurance density in year 2014 was Rs 4,831 (approx. USD 36), an increase of 4% compared to Rs 4, 637 in 2013, out of which Rs 2,157 had been for life insurance. From the above figures it is evident that the development of the sector is also dependent on the spending power/higher disposable income of people.</p>

                                    <p itemprop="description"><b>Q: Are there regulatory laws IBSL has introduced in recent times to safeguard policyholders?</b></p>
                                    <p itemprop="description">A: There are a few regulatory laws we have introduced:
                                    <ul class="p-ul">
                                        <li>An amendment to section 98 of the Regulation of Insurance industry Act, No. 43 of 2000 was introduced to empower the Insurance Board, the regulator of the insurance industry to hear and decide disputes arising out of general insurance business among policyholders and insurance companies. This is in addition to dispute resolution of long term insurance business.</li>
                                        <li>Segregation of composite insurance companies is another requirement introduced, and the Insurance Board expects greater focus on business lines, greater transparency, visibility on profitability and greater policyholder protection by implementation of this requirement.</li>
                                        <li>Move from solvency margin regime to Risk Based Capital regime from year 2016 onwards will require insurance companies to determine their capital based on their risk appetite. </li>
                                        <li>The Insurance Board has also required insurance companies to increase their stated capital from Rs 100 million to Rs 500 million.</li>
                                    </ul>
                                    </p>
                                    <p itemprop="description"><b>Q: As you see it, how is the insurance industry in Sri Lanka faring in general? </b></p>
                                    <p itemprop="description">A: The industry witnessed an overall total insurance premium income growth of 5.14% in 2014 recording a value of Rs 99,872 million. Out of same, the value recorded for long term insurance business was Rs 44,610 million.  A much slower growth was witnessed in the General Insurance Business compared to Long Term Insurance Business. Total assets of the industry amounted to Rs 406,827 million, out of which Rs 249,850 million accounted for Long Term Insurance business. Total assets of the insurance sector accounted for 3.4% of the total assets of the financial sector.    </p>
                                    <p itemprop="description">Total assets of the Industry have grown by 0.84% as at Q2, 2015, when compared to Q2, 2014. For the same period, insurance premium growth for Life was 11.83%, General was 11.66%, witnessing an overall growth of 11.73%. Solvency for Life insurance increased from 8.69 to 9.27 and for General insurance from 2.19 to 2.23 as at Q2, 2015 when compared to Q2, 2014. </p>

                                    <p itemprop="description"><b>Q: Can you indicate the value of claims paid by the insurance industry on an average year?</b></p>
                                    <p itemprop="description">A: The value of claims incurred by insurers carrying on long term insurance business for year 2014 amounted to Rs 20,685 million, which represented an increase of 12.97% compared to 2013.   The total value of claims paid by the insurance industry for year 2014 was around 50, 000 million.  </p>

                                    <p itemprop="description"><b>Q: Can you please elaborate on the role played by IBSL in developing promoting and safeguarding the life insurance industry?</b></p>
                                    <p itemprop="description">A: The Insurance Board of Sri Lanka was established by the Regulation of Insurance Industry Act, No. 43 of 2000 in March 2001 for the purpose of developing, supervising and regulating the insurance industry in Sri Lanka. Accordingly, all activities of the Insurance Board are carried on for the above purpose with the object and responsibility of ensuring that the insurance sector carries on business with integrity and in a professional and prudent manner with a view to safeguarding the interests of policyholders and potential policyholders</p>
                                    <p itemprop="description">The Insurance Board through its Market Development and External Relations unit carries out various awareness campaigns on the importance of insurance throughout the island to promote and develop the sector. We also publish posters, brochures, notices and articles containing educational material on insurance.   </p>

                                    <p itemprop="description"><b>Q: How is the response for the Insurance Month and what do you think would be the outcome? </b></p>
                                    <p itemprop="description">A: As informed by IASL, all 15 insurance companies licensed by the Insurance Board to carry on Long Term Insurance Business have joined hands in this massive campaign and they expect to reach one million people during the month of September and introduce around 75,000 new life insurance policies. I congratulate the Insurance Association of Sri Lanka for initiating an Insurance Awareness Month to promote and develop the Life insurance industry and wish all insurance companies participating in this campaign, every success. </p>

