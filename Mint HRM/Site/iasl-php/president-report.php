                                <h4>Matters Pertaining to the Ministry of Finance & Planning (MoF) </h4>
                                <p>Members of the Finance Technical Sub Committee (FTSC) are continuing discussions with representatives of the MoF and Inland Revenue Department (IRD) to arrive at a sustainable basis for taxing life company profits. The discussions followed the budget proposal to define management expenses for the life business. </p>
                                <p>In addition, the IASL continued to lobby for state business to be opened to private sector insurers (based on a suitable rating) and to resolve the long standing dispute related to VAT on reinsurance recoveries on claims and reinsurance commissions.  </p>
                                <p>The budget proposal to withdraw luxury and semi luxury taxes which is currently being collected by general insurers may not be enacted. </p>
                                <p>Preliminary discussions were also had with regard to IASL supporting to set up a viable pension scheme in Sri Lanka. This matter will be pursued at the appropriate time.</p>

                                <h4>Activities of the Life Insurance Forum (LIF)  </h4>
                                <p>Members of the LIF continued to play a vital role in supporting the IASL ExCo on technical matters pertaining to the life business. </p>

                                <h5>Achievements</h5>
                                <p>The LIF finalised a pool of 213 doctors and 236 laboratories to conduct medical examinations at the point of underwriting a policy for the entire life insurance industry. This was an outcome of a national level initiative which was supported by all insurance companies, who willingly shared the resources used by respective companies. This initiative is expected to enhance customer service while reducing underwriting leakage. </p>
                                <p>At the request of the IBSL, and facilitated by LIF, life insurers agreed to implement a common / standard cooling off period for life insurance policies. To facilitate this change, IBSL extended the time period to hand over policies to customers to 14 days (instead of 7 days). </p>
                                <p>LIF members commenced sharing life insurance premiums on an accrual basis to ensure that figures shared among members were consistent with figures published by listed companies.</p>

                                <h5>Continuous Professional Development (CPD) </h5>
                                <p>As in previous years, the LIF conducted CPD and other programmes with the support of the Sri Lanka Insurance Institute (SLII) facilitated by resource persons from Munich Re, Hannover Re and RGA. These programmes catered to entry level as well as senior technical staff involved in underwriting and claims functions. </p>

