                                <p>As per information shared among IASL members, life and general insurance companies reported 20% and 15% year on year growth as at December 2015. Higher disposable income and access to affordable credit facilities which boosted motor vehicle sales were the main contributors to this growth momentum.</p>
                                <div class="row" style="margin-bottom: 15px;">
                                    <div class="col-md-5">
                                        <p>As per figures published by the Insurance Board of Sri Lanka (IBSL), life and general insurance premiums accounted for 1.02% of Sri Lanka’s GDP and insurance company assets accounted for 3.4% of financial sector assets (2014 data). While these figures are relatively low, they serve to highlight the vast potential for growth and development in the sector</p>
                                    </div>
                                    <div class="col-md-4">
                                        <img src="images/news/chart.png">
                                    </div>
                                </div>
                                <p>The insurance industry provides direct employment to over 16,000 staff, operate from 1,462 branches spread throughout the island and have engaged the services of approximately 43,000 life and general insurance agents to distribute their products (2014 data).</p>
                                <p>These activities have resulted in 30% of the working population having some form of life insurance (2.6 million life insurance policies are in force as at December 2014). There are 4.6 million general insurance policies in force, including 2.2 million 3rd party motor insurance policies which is a compulsory requirement in Sri Lanka (2014 data). </p>
                                <p>Despite the relatively low penetration levels, the above statistics clearly reflect the significant impact the insurance industry has made on the socio-economic landscape of Sri Lanka.</p>
