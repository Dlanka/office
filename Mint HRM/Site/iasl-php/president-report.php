                                <p>The NITF successfully negotiated a reinsurance programme to mitigate the risk associated with the 30% compulsory cession. In a move welcomed by all general insurers, a profit commission scheme for non motor SRCC and Terrorism fund was reintroduced in 2016. Several other technical matters were also resolved between IASL and NITF in an amicable manner. </p>
