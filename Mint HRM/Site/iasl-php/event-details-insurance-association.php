                                    <h3 class="post-title" itemprop="name">The Insurance Association of Sri Lanka (IASL) launches Life Insurance Month 1st to 30th September 2015</h3>
                                    <h5>01 September 2015</h5>
                                    <div class="event-org">
                                        <!--<h4>Awareness Campaign</h4>-->
                                    </div> 
                                    <p>What does ‘protection’ mean to you? How important is your family to you? Have you adequately provided for them when you are no longer around?   In order to make sure and remind you of the need to include life insurance in your financial plans, the IASL in concurrence with the Insurance Board of Sri Lanka will be launching a ‘Life Insurance Awareness Month’ during the month of September.  </p>
                                    <p>If disaster strikes, life insurance can pay out when your family needs it most. It can pay off debts and loans, providing surviving family members with the chance to move on with a clean slate. It can keep families in their homes and pre-fund a child’s education. It can keep a family business in the family. It can provide a stream of income for a family to live on for a period of time. </p>
                                    <p>According to statistics 12.6% Sri Lankan’s are protected with life insurance. It is known that a majority of families either have no life insurance or not enough, leaving them one accident or terminal illness away from a financial catastrophe for their loved ones. The life insurance market is highly under-penetrated with the total life insurance premium to Gross Domestic Product ratio standing at only 0.46% in  2014 (IBSL Annual Report).</p>
                                    <p>IBSL Director General Mrs. Damayanthi Fernando endorsed this campaign stating it to be “a timely and necessary industry initiative,” adding that “enhancing life insurance penetration has been and continues to be one of the biggest challenges before us. With the ever-changing socio-economic landscape and shifting demographics in our country, we encourage the industry to pursue such forward-thinking programmes which will educate and create a greater awareness about Life insurance among the general public.”</p>
                                    <p>President of the IASL, Mr. Dirk Pereira said, “this is a significant initiative which brings together all life insurance companies to address a national need. The campaign will also address issues which will have a long term positive impact not only for the industry, but for the country as a whole.” He added, “as insurers our role is to help protect you and your loved ones and that is the underlining objective in this campaign…to urge people to take that extra step to ensure protection...to get life and live life.”</p>
                                    <p>Chairman of the Marketing & Sales Forum of IASL, Mr. Samitha Hemachandra said, “The coming together of life insurance advisors from all companies for the common cause of improving awareness in the market regarding the value of life insurance is a noteworthy development for the industry.”</p>
