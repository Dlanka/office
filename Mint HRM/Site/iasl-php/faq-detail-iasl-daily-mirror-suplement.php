                                <p>The Insurance Association of Sri Lanka (IASL) was founded in 1989. Since then, the Association has evolved to include the membership of every insurance company in the country, with the objectives of creating solutions for the common issues relevant to the insurance industry.</p>
                                <p>Mr. Samitha Hemachandra- Chairman Marketing and Sales Forum at the IASL went in to detail regarding the functions of the Marketing and Sales Forum within the IASL, the insurance penetration in the country and the role the IASL plays within the industry. </p>
                                <p>The insurance companies, working towards achieving a united goal as the Insurance Association represent various issues in the insurance industry. As the Marketing and Sales forum, it is the task of Mr. Hemachandra and his colleagues to promote insurance within the country and to solve the issues related to the sales, marketing and  the customers alike. </p>
                                <p>One of the biggest challenges that the insurance industry faces as a whole is the low penetration, largely stemming from the people's lack of understanding towards Life Insurance.. While policy holders realize the importance of general insurance and motor insurance, life insurance is an area where many people neglect to consider.</p>
                                <p>To counter the low penetration within the industry, the IASL has determined to create awareness among the people. With the support of the entire Association behind them, the Marketing and Sales Forum has planned a life insurance awareness month to be held throughout the month of September, 2015. </p>
                                <p>Irrespective of their individual companies, each member Life Insurance company of the IASL would work towards introducing life insurance to the public. The objective of the Association is to make the public aware of the importance of obtaining a life insurance policy.</p>
                                <p>Having started the campaign on the 1st September at the Independence Arcade, with the Chairperson and the Director-General of the Insurance Board of Sri Lanka (IBSL), executive committee of the IASL, CEOs of all Life Insurance Companies and over 1000 Insurance advisors  from the industry in attendance, the month of September will comprise of town-to-town awareness events. </p>
                                <p>Mr. Hemechandra stated that the Association plans to reach at least 1 million people and create awareness among them. In addition, the Association hopes to sell at least 75 000 life policies within the month. </p>
                                <p>Through this campaign, the IASL is encouraging people to "purchase a life insurance on behalf of their loved ones". Mr. Hemachandra further went on to say that in time to come, the IASL hopes to ensure that every Sri Lankan is covered with life insurance. </p>

