                                    <h4 class="text-center">
                                        <i>Only Sri Lankan Company to be awarded at this year's edition of region's premier <br>insurance industry awards ceremony</i>
                                    </h4>
                                    <p></p>
                                    <p>Sharing the stage with some of Asia's giants in  insurance at the gala dinner held in Singapore recently,  which honoured winners from 15 categories, the award was received on behalf of Asian Alliance Insurance by Managing Director, Iftikar Ahamed. In total, the 19 th Asia Insurance Industry Awards attracted  over  400 entries  and 47  finalists were selected for a rigorous judging process  – which was audited by KPMG and was undertaken by a panel of 20 top industry professionals.</p>
                                    <p>"For its innovation in eliminating the traditional accident claims process in motor insurance and for bringing revolutionary change to Sri Lanka's motor Insurance Industry, Asian Alliance Insurance PLC has won the Innovation of the Year award," the Asia Insurance Review stated.</p>
                                    <p>Asian Alliance Insurance was the only Sri Lankan as well as South Asian finalist in the 'Innovation of the year category', this year in which the other finalists were Zurich Insurance, Hong Kong and QBE Asia Pacific, Hong Kong. It was also the only Sri Lankan insurer to be a finalist and to emerge victorious in any category at this year's edition.</p>
                                    <p>"This recognition – at the region's premier awards forum, largely dominated by Asia's insurance sector giants – is possibly Asian Alliance Insurance's proudest moment to date,” Managing Director, Iftikar Ahamed said. “We take great pride in this achievement as it strongly underscores our constant pursuit of excellence via innovation to enhance customer convenience, the world-class fundamentals of the company and most importantly, the strength and capability of our dynamic team – to whom I dedicate this honour."</p>
                                    <p>Asian Alliance's ground-breaking innovative motor product 'Click2Claim' won this prestige for the most innovative product or service introduced during the qualifying period with clear and measurable impact on Asia's industry or possible region-wide application; and/or disruption which is leading the industry's push to the next level.</p>
                                    <p>By enabling claims to be submitted via a smartphone app and thus eliminating the need for waiting for  assessors  to  visit,  Asian  Alliance  Insurance's  'Click2Claim'  offers  unparalleled  benefits  and  a substantial time saving in submitting a motor insurance claim. It is thus ideally suited to the hectic lifestyles of today's customers. In addition, owners of cars and dual-purpose vehicles can now purchase Click2Claim motor insurance policies online.</p>
                                    <p>Now in its 19th year, the Asian Insurance Industry Awards has become a much sought-after mark of excellence and peer group endorsement for insurance players and professionals in the market. The Awards have been hosted annually since 1997 by Asian Insurance Review, the voice of Asia's insurance industry for the past 25 years.</p>
                                    <p>Asian Alliance Insurance is part of the Softlogic Group, which is recognized as one of Sri Lanka's most diversified and fastest-growing conglomerates with interests in Healthcare, Retail, ICT, Leisure, Automobiles and Financial Services. Significant stakeholders in the company also include FMO and DEG that are rated 'AAA' Development Financial Institutions from the Netherlands and Germany respectively.</p>

