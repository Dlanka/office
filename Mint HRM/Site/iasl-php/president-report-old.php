                                <h4>Insurance Awareness Program</h4>
                                <p>Proposed by Chairperson MSF a continuing communication strategy program in place of one time awareness program is being considered by the IASL. Accordingly, IASL discussed the paper submitted by the MSF and requested MSF to fine-tune same and present to IASL Exco along with an expenditure budget</p>

                                <h4>Insurance Ombudsman Scheme</h4>
                                <p>IASL continues with the Insurance Ombudsman scheme and on behalf of the members I express my utmost satisfaction with the functioning of the scheme and the benefits derived by both Insurer and the Insured. With the approval of the Executive Council we need to extend Dr. Wickrema Weerasooria’s contract for a further period of one year commencing 1st March 2015.</p>
                                <p>IASL Exco extended permission for him to function as an advisor to the President of DSRSL on Finance mainly on areas of Banking and Insurance in addition to his services as Insurance Ombudsman</p>

                                <h4>IASL Monthly and Annual Accounts</h4>
                                <p>IASL Monthly Accounts prepared at the IASL Secretariat was presented at the Executive Council Meetings commencing July 2011. However since 2014 February, IASL secretariat could not submit monthly accounts due to untimely demise of the book keeper Mr. Dinesh Bandara from Asian Alliance Insurance PLC who passed away under tragic circumstances </p>
                                <p>Annual Audited Accounts for the year ended 31st December 2014 will be presented at the Annual General Meeting scheduled to take place on Wednesday 18th March 2015.</p>
                                <p>I am pleased to state that IASL investments have increased to Rs. 12.00 million placed with Lanka Orix Finance Co. Ltd. and Nations Trust Bank.</p>
                                <p>Secretariat has also streamlined the Accounting system by preparing and presenting an Income and Expenditure Budget for 2014 on a monthly and monthly cumulative basis.</p>

                                <h4>Appreciation</h4>
                                <p>Chairperson, Director General and other sectional heads of the regulatory body IBSL has been supportive of all issues confronted by the Insurance Industry and their understanding and guidance is very much appreciated. </p>
                                <p>Insurance Ombudsman Dr. Weerasooria has not only functioned as an Ombudsman but as a great supporter of the Insurance Industry who published a couple of books on legal aspects of Insurance Industry. We appreciate the role he plays as the Ombudsman and convey our sincere thanks for the guidance and support,</p>
                                <p>President and the Council of the Sri Lanka  Insurance Institute( SLII ), President and members of the Sri Lanka Insurance Brokers Association and Chairman, Secretary General and Staff of Ceylon Chamber of Commerce (CCC)  deserves our utmost appreciation of the cooperation and support extended to us in all matters concerning the Insurance Industry.</p>
