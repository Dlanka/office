                                <p>As in the previous year, the very capable Chairpersons of the IASL technical subcommittees carried out their role in their relevant areas of expertise, exchanging ideas and sharing information among their respective representatives from member companies. As in the past they interacted with the regulator and other Government authorities safeguarding and furthering the interest of the Insurance Industry as a whole. IASL Office bearers, members of the Executive Committee and the subcommittee Chairpersons met at least once a month regularly to review the progress of initiatives as well as the issues confronted in an efficient manner. I believe that the continued engagement of subcommittees with Industry professionals at the helm will be the way forward to take the Insurance Industry into better focus of the public and to resolve issues with authorities that may affect the industry.</p>
                                <h4>Sub Committee Meetings</h4>
                                <p>Most Sub Committees met on regular monthly basis to discuss issues pertaining to their area of relevance. Secretary General coordinated and attended all subcommittee meetings. Minutes of such meetings were circulated among all members of the Executive Committee for their information and advice. Further, Sub Committee Chairpersons were invited to participate and report at the regular monthly executive committee meetings.</p>

                                <h4>Secretary General</h4>
                                <p>Mr. Upali Samarasinghe, the Secretary General who succeeded Mr. Roy Balasuriya in 2011 has continued in his role as the key coordinator of IASL activities, liaising with the IASL office bearers /members of the Executive Committee and the chairpersons of subcommittees. Introducing the monthly reporting system of IASL financial statements compared with corresponding previous year accounts, Annual budget, and largely enhancing the investment portfolio of the IASL were some of his achievements in his capacity as the Secretary General of our Association. I extend my sincere thanks to him.</p>

                                <h4>Meetings</h4>
                                <h5>The Executive Committee Meetings</h5>
                                <p>Twelve regular monthly executive committee meetings were held during the period under review, well attended by all member companies of the Association. Current status relating to the regulatory issues and matters relating to Industry progress and wellbeing were discussed. Cooperation and commitment shown by the members and the alternates who attend the meeting is praiseworthy</p>

                                <h5>Growth of the Insurance Industry</h5>
                                <p>Gross Written Premium for the year ended 31st December 2013 was Rs. 94.48 Billion compared to Rs. 87.17 Billion in 2012, a growth of Rs. 7.3 Billion or 8.39 percent. Industry which recorded 10.33 % growth in Life Insurance and 9.12% growth in General Insurance business year on year 2012/2013, slowed year on year 2013/2014 recording Rs. 41.31 Billion or 10.21 % growth in Life Business and Rs. 53.18 Billion or 7.02% growth in General Business .Though both areas showed progress, year to year growth as a percentage compared to previous year shows a downward trend</p>
                                <p>I salute the member Company CEOs, the members of the Executive Council and the Chairpersons and members of the seven sub committees who worked tirelessly to ensure this result.</p>

                                <h5>Major Issues faced by the Industry</h5>
                                <p>With the improvement of the macroeconomic environment, rapid development taking place on many spheres of economic activity, the period under review was full of challenges and obstacles. We were able to overcome most of these challenges if not all due to the cohesiveness of the membership whose cooperation in arriving at decisions was noteworthy. I shall append below some of the issues that the Industry faced during the period under review and their current status.</p>

