                                    <p>For the first time in history, Sri Lanka will see a gathering of its General Insurance companies under one roof and in a common forum. The 10th of May 2016 will see the General Insurance Sales Congress (GISCO) taking place at Waters Edge under the fitting theme, ‘Growth through Innovation', chosen specially to encourage companies to pay more attention to sustainable innovation in order to achieve competitive advantage. </p>
                                    <p>The principal motivations behind the organization of such an event are; the need to provide the General Insurance fraternity with an opportunity to share key insights and knowledge on the importance of innovation to gain customer loyalty, to gather different perspectives from international and local insurance experts as well as to obtain views from both sales and non-sales staff on innovation and sustainability. </p>
                                    <p>The Chairperson of the Insurance Board of Sri Lanka, Mrs. Indrani Sugathadasa will grace the event as chief guest, with other distinguished personalities of the insurance industry including, Mrs. Damayanthi Fernando, Director General of IBSL and Dr. Christian Bartsch of Munich Re. </p>
                                    <p>The keynote speech delivered by Dr. Bartsch will be one of the primary points of the morning agenda along with Mr. Chandra Schaffter's address on ‘Growth through Innovation' and two special presentations conducted by Mr. A. Gunwardena, CEO of Ceylinco General and Mr. Ramal Jasinghe, CEO of Asian Alliance Insurance, as well as a panel discussion. Following the lunch break, the afternoon's session will be led by a motivational speech made by Mr. Dammika Kalapuge. A second panel discussion will bring the day's program to an end. </p>
                                    <p>"The insurance industry is constantly evolving, and this forum will provide a useful platform to exchange ideas and gain insights from both local and international experts", said Mr. Dirk Perera, President of the Insurance Association of Sri Lanka (IASL).</p>
                                    <p>"It is a tremendous honor to be part of the organizing committee of the first General Insurance Sales Congress", stated Mr. Samitha Hemachandra, Chairman of the Marketing and Sales Forum of IASL. “The fundamental purpose behind this event is to inspire and encourage innovation amongst the General Insurance fraternity of Sri Lanka so as to ensure the growth of the country's insurance industry".</p>

